# Function to fetch and parse a webpage with a User-Agent header


def fetch_page(url):
    headers = {
        'User-Agent': 'Mozilla/5.0 (Windows NT 10.0; Win64; x64) AppleWebKit/537.36 (KHTML, like Gecko) Chrome/86.0.4240.183 Safari/537.36'
    }
    try:
        # Add headers and timeout
        response = requests.get(url, headers=headers, timeout=10)
        response.raise_for_status()
        return response.text
    except requests.exceptions.RequestException as e:
        print(f"Error fetching {url}: {e}")
        return None

# Function to detect language of the webpage content


def detect_language(html_content):
    soup = BeautifulSoup(html_content, 'html.parser')
    text = soup.get_text()[:1000]  # Limit text to the first 1000 characters
    try:
        language = detect(text)
        return language
    except Exception as e:
        print(f"Error detecting language: {e}")
        return "unknown"


