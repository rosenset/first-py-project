# Function to fetch and parse a webpage
def fetch_page(url):
    try:
        response = requests.get(url, timeout=10)  # Add timeout to requests
        response.raise_for_status()
        return response.text
    except requests.exceptions.RequestException as e:
        print(f"Error fetching {url}: {e}")
        return None

# Function to detect language of the webpage content
def detect_language(html_content):
    soup = BeautifulSoup(html_content, 'html.parser')
    text = soup.get_text()[:1000]  # Limit text to first 1000 characters
    try:
        language = detect(text)
        return language
    except Exception as e:
        print(f"Error detecting language: {e}")
        return "unknown"

